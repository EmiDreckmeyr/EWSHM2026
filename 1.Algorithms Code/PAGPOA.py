def lora_distance_with_repulsion(X, centers, min_dist, repulsion_strength):
    """GENTLE LoRa repulsion - soft penalty for close nodes."""
    euclidean_dist = np.linalg.norm(X[:, None, :] - centers[None, :, :], axis=2)

    # Soft repulsion: quadratic penalty, activates <15m
    too_close = np.minimum(euclidean_dist, min_dist)
    repulsion = repulsion_strength * ((min_dist - too_close) / min_dist) ** 2

    return euclidean_dist + repulsion

def gentle_min_distance_push(centers, X, min_dist=15.0, push_factor=0.5):
    """GENTLE push away from nodes - no hard constraint."""
    centers = centers.copy()

    for i in range(len(centers)):
        dists = np.linalg.norm(X - centers[i], axis=1)
        nearest_node_idx = np.argmin(dists)
        current_dist = dists[nearest_node_idx]

        if current_dist < min_dist:
            # Gentle proportional push
            push_distance = (min_dist - current_dist) * push_factor
            direction = centers[i] - X[nearest_node_idx]
            direction /= np.linalg.norm(direction) + 1e-8
            centers[i] = X[nearest_node_idx] + direction * (current_dist + push_distance)

    return centers

def enforce_gateway_elevation(centers, X_nodes, min_elev_above_nodes):
    """Ensure ALL gateways are elevated above highest node + buffer."""
    centers = centers.copy()

    max_node_height = np.max(X_nodes[:, 2])
    target_height = max_node_height + min_elev_above_nodes

    # Preserve XY cluster centers, elevate Z only
    centers[:, 2] = np.maximum(centers[:, 2], target_height)

    print(f"🗼 Elevated gateways: max_node_h={max_node_height:.1f}m → target_h={target_height:.1f}m")
    print(f" New heights: {centers[:, 2].round(1)}m")

    return centers

def propagation_c_max(X, node_ratio=8, hard_cap=5):
    """Estimate max clusters based on node count and coverage."""
    N = len(X)
    span_xy = np.max(np.linalg.norm(X[:, :2] - np.mean(X[:, :2], axis=0), axis=1)) * 2 + 100
    C_nodes = max(1, N // node_ratio)
    return min(C_nodes, hard_cap) + 1

