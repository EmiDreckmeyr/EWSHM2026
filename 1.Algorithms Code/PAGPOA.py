# 8. C* SEARCH WITH PROPAGATION + LoRa CONSTRAINTS
# ============================================================

def find_optimal_C_propagation(X, interpolator, config):
    C_max = min(
        config['C_max'],
        propagation_c_max(X, config['node_ratio'], config['hard_cap'])
    )
    C_min = config['C_min']

    N = len(X)
    max_nodes_per_gw = 40
    C_min = max(C_min, int(np.ceil(N / max_nodes_per_gw))- 1)

    best_C = C_min
    best_xb = np.inf

    print(f"\n🔍 Propagation+LoRa search for C in [{C_min}, {C_max}]")

    for C in range(C_min, C_max + 1):
        centers, U, iters = fcm_propagation_aware_lora(
            X,
            C,
            config,
            interpolator,
            max_iter=100,
            seed=42+C
        )

        centers = gentle_min_distance_push(centers, X, config['min_dist_penalty'])
        centers = enforce_gateway_elevation(centers, X, config['min_elev_above_nodes'])

        xb = xie_beni_index_prop(X, centers, U, config, interpolator)

        print(f" C={C}: XB_prop={xb:.6e} (iters={iters})")

        if xb < best_xb and np.isfinite(xb):
            best_xb = xb
            best_C = C

    print(f"\n✅ Propagation+LoRa OPTIMAL C* = {best_C} (XB_prop={best_xb:.6e})")
    return best_C, best_xb

