class RadioMobileInterpolator:
    def __init__(self, df_coverage, gateway_positions, lat0, lon0, h0, P_sens):
        self.df = df_coverage
        self.gateways = gateway_positions
        self.origin = (lat0, lon0, h0)
        self.P_sens = P_sens

        self.X_coverage = latlon_to_local_enu(
            self.df['latitude'],
            self.df['longitude'],
            np.zeros_like(self.df['latitude']),
            lat0, lon0, h0
        )

        self.X_gateways = latlon_to_local_enu(
            self.gateways['latitude'],
            self.gateways['longitude'],
            self.gateways['height_m'].fillna(180.0),
            lat0, lon0, h0
        )

        print(f"🔍 Interpolator ready: {len(self.df)} points, {len(self.gateways)} gateways")

    def get_rssi_nearest(self, node_xyz, gw_idx):
        distances = np.linalg.norm(self.X_coverage - node_xyz, axis=1)

        k = 5
        idx = np.argsort(distances)[:k]

        d = distances[idx]
        weights = 1.0 / (d + 1e-6)
        weights /= np.sum(weights)

        return np.sum(weights * self.df.iloc[idx]['rssi_dbm'].values)

    def get_rssi_from_map(self, node_xyz, gw_xyz):
        gw_distances = np.linalg.norm(self.X_gateways - gw_xyz, axis=1)
        gw_idx = np.argmin(gw_distances)
        return self.get_rssi_nearest(node_xyz, gw_idx)

def propagation_cost_matrix(
    X_nodes,
    centers,
    interpolator,
    max_range_m=800,
    range_soft_m=500,
    M_soft=25.0,
    K_bad=100.0,
    eps=1e-3
):
    """
    RSSI cost × range penalty.
    """
    N = X_nodes.shape[0]
    C = centers.shape[0]
    D = np.zeros((N, C), dtype=float)

    for i in range(N):
        for j in range(C):
            dist_ij = np.linalg.norm(X_nodes[i] - centers[j])

            if dist_ij > max_range_m:
                D[i, j] = K_bad * 10
            else:
                range_penalty = 1.0 + (dist_ij - range_soft_m) / (max_range_m - range_soft_m)
                range_penalty = np.maximum(range_penalty, 1.0)

                P_ij = interpolator.get_rssi_from_map(X_nodes[i], centers[j])
                M_ij = P_ij - interpolator.P_sens

                if M_ij <= 0:
                    rssi_cost = np.exp(-M_ij / 5.0)
                else:
                    M_eff = M_soft * (1.0 - np.exp(-M_ij / M_soft))
                    rssi_cost = 1.0 / (M_eff + eps)

                D[i, j] = rssi_cost * range_penalty

    D = np.clip(D, 1e-6, np.percentile(D, 95))
    D = D / (np.median(D) + 1e-6)

    return D

