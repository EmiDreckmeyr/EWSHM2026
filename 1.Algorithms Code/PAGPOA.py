# 7. PROPAGATION-AWARE FCM WITH LoRa REPLUSION
# ============================================================

def fcm_propagation_aware_lora(
    X,
    n_clusters,
    config,
    interpolator,
    error=1e-5,
    max_iter=300,
    seed=42
):
    """Propagation-aware FCM WITH LoRa repulsion."""
    X = np.asarray(X, dtype=float)
    N, D_dim = X.shape
    n_clusters = int(n_clusters)

    np.random.seed(int(seed))
    U = np.random.uniform(0.0, 1.0, size=(N, n_clusters)).astype(np.float64)
    U = normalize_columns(U)
    U = np.fmax(U, np.finfo(np.float64).eps)

    Um = U ** config['m']
    centers = (Um.T @ X) / np.maximum(Um.sum(axis=0)[:, None], np.finfo(np.float64).eps)

    for it in range(max_iter):
        U_old = U.copy()
        centers_old = centers.copy()

        prop_cost = propagation_cost_matrix(X, centers, interpolator)
        repulsion_cost = lora_distance_with_repulsion(
            X,
            centers,
            config['min_dist_penalty'],
            config['repulsion_strength']
        )

        D = prop_cost * (1 + 0.3 * repulsion_cost)

        D = np.fmax(D, np.finfo(np.float64).eps)
        D = range_limit(D, config['max_dist'])

        exponent = -2.0 / (config['m'] - 1.0)
        U = normalize_power_columns(D, exponent)

        Um = U ** config['m']
        centers = (Um.T @ X) / np.maximum(Um.sum(axis=0)[:, None], np.finfo(np.float64).eps)

        delta_u = np.linalg.norm(U - U_old)
        delta_c = np.linalg.norm(centers - centers_old)

        if delta_u < error and delta_c < error:
            print(f" Converged at iteration {it+1}")
            break

    return centers, U, it + 1

def xie_beni_index_prop(X, centers, U, config, interpolator=None):
    N = X.shape[0]

    prop_cost = propagation_cost_matrix(X, centers, interpolator)
    D = np.fmax(prop_cost, np.finfo(np.float64).eps)

    prop_weights = 1 / (1 + np.mean(D, axis=0))  # Favor C with better avg RSSI
    num = np.sum((U ** config['m']) * (D ** 2))

    # amplify bad links
    num *= (1 + 0.5 * np.std(D))

    C = centers.shape[0]
    if C < 2:
        return 1e6

    center_dists = np.linalg.norm(centers[:, None, :] - centers[None, :, :], axis=2)
    mask = ~np.eye(C, dtype=bool)

    dists2 = center_dists[mask] ** 2

    # --- soft-min separation (more stable in dense deployments) ---
    k = max(1, len(dists2) // 4)
    min_center_dist2 = np.mean(np.sort(dists2)[:k])

    # --- floor to prevent collapse ---
    expected_spacing = (np.max(X[:,0]) - np.min(X[:,0])) / centers.shape[0]
    min_center_dist2 = max(min_center_dist2, (0.5 * expected_spacing) ** 2)

    if min_center_dist2 < 1e-6:
        return 1e6

    max_nodes_per_gw = config.get('max_nodes_per_gw', 30.0)
    cluster_load = np.sum(U, axis=0)
    avg_load = N / centers.shape[0]

    density_penalty = np.mean((cluster_load / avg_load) ** 2)
    if N < 99:
      c_bias = C / np.log(N + 1)
    else:
      alpha = 1.5
      c_bias = (C ** alpha) / np.log(N + 1)

    return num / (min_center_dist2 * density_penalty * c_bias)

