    print(f"🌍 Origin: lat={lat0:.6f}, lon={lon0:.6f}, h={height0:.1f}m")
    print(f"📊 Loaded {len(df)} nodes")

    X = latlon_to_local_enu(lat, lon, height, lat0, lon0, height0)

    return X, (lat0, lon0, height0), df['node_id'].values

