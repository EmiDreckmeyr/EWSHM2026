    output_dir = config['output_dir']
    os.makedirs(output_dir, exist_ok=True)

    print("=== 🚀 Propagation+LoRa Bridge Optimizer (Repulsion + Height) ===")

    X_nodes, origin, node_ids = load_nodes_csv_to_xyz(config['csv_file'])
    lat0, lon0, h0 = origin

    df_coverage = parse_radiomobile_txt(config['txt_file'], config['gateway_ids'])
    plot_coverage_map(df_coverage, show_plots=config['show_plots'])

    gateway_positions = pd.DataFrame({
        'node_id': config['gateway_ids'],
        'latitude': [43.37778, 43.37774, 43.37701, 43.37766, 43.37762],
        'longitude': [1.29000, 1.29005, 1.29010, 1.29014, 1.29019],
        'height_m': [180.8, 180.0, 179.0, 178.5, 177.8]
    })

    interpolator = RadioMobileInterpolator(
        df_coverage,
        gateway_positions,
        lat0,
        lon0,
        h0,
        config['P_sens']
    )

    nodes_enu_df = pd.DataFrame({
        'node_id': node_ids,
        'x_enu_m': X_nodes[:, 0],
        'y_enu_m': X_nodes[:, 1],
        'z_enu_m': X_nodes[:, 2],
        'origin_lat': lat0,
        'origin_lon': lon0,
        'origin_height': h0
    })

    nodes_enu_df.to_csv(os.path.join(output_dir, 'prop_nodes_enu.csv'), index=False)

    plot_nodes_enu(X_nodes, "Propagation+LoRa Nodes", config['show_plots'])

    C_star, xb_star = find_optimal_C_propagation(X_nodes, interpolator, config)

    centers, U, iters = fcm_propagation_aware_lora(
        X_nodes,
        C_star,
        config,
        interpolator,
        max_iter=300,
        seed=42
    )

    centers = gentle_min_distance_push(centers, X_nodes, config['min_dist_penalty'])
    centers = enforce_gateway_elevation(centers, X_nodes, config['min_elev_above_nodes'])

    min_dist_to_nodes = np.min(
        np.linalg.norm(
            X_nodes[:, None, :] - centers[None, :, :],
            axis=2
        ),
        axis=0
    )

    print(f"\n✅ FINAL: C*={C_star}, XB_prop={xb_star:.2e}, iters={iters}")
    print(f" Min gw-node distance: {min_dist_to_nodes.min():.1f}m (target >{config['min_dist_penalty']}m)")

    plot_nodes_and_gateways(X_nodes, centers, C_star, config['show_plots'])

    lat_gw, lon_gw, h_gw = local_enu_to_latlon(
        centers[:,0],
        centers[:,1],
        centers[:,2],
        lat0,
        lon0,
        h0
    )

    pd.DataFrame({
        'gateway_id': range(C_star),
        'longitude': lon_gw,
        'latitude': lat_gw,
        'height_m': h_gw
    }).to_csv(os.path.join(output_dir, 'prop_gateways_ns3.csv'), index=False)

    pd.DataFrame({
        'gateway_id': range(C_star),
        'x_enu_m': centers[:,0],
        'y_enu_m': centers[:,1],
        'z_enu_m': centers[:,2],
        'origin_lat': lat0,
        'origin_lon': lon0,
        'origin_height': h0
    }).to_csv(os.path.join(output_dir, 'prop_gateways_enu.csv'), index=False)

    print(f"\n✅ SUCCESS! Files saved to: {output_dir}")
    print(" Files: prop_gateways_ns3.csv, prop_gateways_enu.csv, prop_nodes_enu.csv")

    return C_star, centers, U, interpolator

