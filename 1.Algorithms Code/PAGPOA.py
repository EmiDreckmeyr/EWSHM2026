def parse_radiomobile_txt(txt_file, gateway_ids):
    """Parse pontdemauzac.txt format."""
    data = []
    in_table = False

    with open(txt_file, 'r') as f:
        for line in f:
            line = line.strip()

            if line.startswith('Latitude') and 'Longitude' in line:
                in_table = True
                continue

            if not in_table or not line or line.startswith('Fixed unit'):
                continue

            parts = re.split(r'\s+', line)
            if len(parts) >= 4:
                try:
                    lat = float(parts[0])
                    lon = float(parts[1])
                    rx_str = parts[2]
                    best_unit = int(parts[3])
                    rx_dbm = float(rx_str)

                    data.append({
                        'latitude': lat,
                        'longitude': lon,
                        'rssi_dbm': rx_dbm,
                        'best_unit': best_unit
                    })
                except (ValueError, IndexError):
                    continue

    df = pd.DataFrame(data)

    print(f"📡 Loaded {len(df)} coverage points from {txt_file}")
    print(f" RSSI range: {df['rssi_dbm'].min():.1f} to {df['rssi_dbm'].max():.1f} dBm")
    print(f" Gateways in use: {sorted(set(df['best_unit']) & set(gateway_ids))}")

    return df

