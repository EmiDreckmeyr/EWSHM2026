def normalize_columns(u):
    """Normalize columns so each column sums to 1 (fuzzy partition matrix)."""
    return u / np.sum(u, axis=1, keepdims=True)

def normalize_power_columns(d, exponent):
    """SciKit‑style safe normalization of distances with exponent."""
    d = d.astype(np.float64)
    d = d / np.max(d, axis=1, keepdims=True)
    d = np.fmax(d, np.finfo(d.dtype).eps)

    if exponent < 0:
        d /= np.min(d, axis=1, keepdims=True)
        d = d ** exponent
    else:
        d = d ** exponent

    return normalize_columns(d)

def range_limit(d, max_dist=2000.0):
    """Cap extreme distances to avoid outliers dominating clustering."""
    d = np.asarray(d)
    d[d >= max_dist] = 10000.0
    return d

