def lora_distance_with_repulsion(X, centers, min_dist, repulsion_strength):
    """GENTLE LoRa repulsion - soft penalty for close nodes."""
    euclidean_dist = np.linalg.norm(X[:, None, :] - centers[None, :, :], axis=2)

    # Soft repulsion: quadratic penalty, activates <15m
    too_close = np.minimum(euclidean_dist, min_dist)
    repulsion = repulsion_strength * ((min_dist - too_close) / min_dist) ** 2

    return euclidean_dist + repulsion

def hybrid_fcm_3d(X, n_clusters, m=2.0, error=1e-5, max_iter=300, seed=42):
    """Original FCM (no LoRa penalty)."""
    X = np.asarray(X, dtype=float)
    N = X.shape[0]

    np.random.seed(seed)
    U = np.random.rand(N, n_clusters)
    U = normalize_columns(U)
    U = np.fmax(U, np.finfo(np.float64).eps)

    for it in range(max_iter):
        U_old = U.copy()
        Um = U ** m
        centers = (Um.T @ X) / Um.sum(axis=0)[:, None]

        D = np.linalg.norm(X[:, None, :] - centers[None, :, :], axis=2)
        D = np.fmax(D, np.finfo(np.float64).eps)
        D = range_limit(D)

        U = normalize_power_columns(D, -2.0 / (m - 1.0))
        if np.linalg.norm(U - U_old) < error:
            break

    return centers, U, it + 1

def hybrid_fcm_3d_lora(X, n_clusters, m=2.0, error=1e-5, max_iter=300,
                      min_dist_penalty=15.0, repulsion_strength=100.0, seed=42):
    """LoRa-aware FCM with GENTLE repulsion."""
    X = np.asarray(X, dtype=float)
    N = X.shape[0]

    np.random.seed(seed)
    U = np.random.rand(N, n_clusters)
    U = normalize_columns(U)
    U = np.fmax(U, np.finfo(np.float64).eps)

    for it in range(max_iter):
        U_old = U.copy()
        Um = U ** m
        centers = (Um.T @ X) / Um.sum(axis=0)[:, None]

        D = lora_distance_with_repulsion(X, centers, min_dist_penalty, repulsion_strength)
        D = range_limit(D)

        U = normalize_power_columns(D, -2.0 / (m - 1.0))
        if np.linalg.norm(U - U_old) < error:
            break

    return centers, U, it + 1

def xie_beni_index_3d(X, centers, U, m=2.0):
    N = X.shape[0]
    D = np.linalg.norm(X[:, None, :] - centers[None, :, :], axis=2)
    num = np.sum((U ** m) * (D ** 2))

    C = centers.shape[0]
    if C < 2:
        return np.inf

    center_dists = np.linalg.norm(centers[:, None, :] - centers[None, :, :], axis=2)
    mask = ~np.eye(C, dtype=bool)
    min_center_dist2 = np.min(center_dists[mask] ** 2)

    return num / (N * min_center_dist2)


