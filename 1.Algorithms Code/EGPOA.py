def lora_bridge_pipeline(config):
    csv_file = config['csv_file']
    output_dir = config['output_dir']

    # Create output directory
    os.makedirs(output_dir, exist_ok=True)

    print("=== 🚀 LoRa Bridge Gateway Optimizer (GENTLE LoRa Penalties + Height) ===")

    X, origin, node_ids = load_nodes_csv_to_xyz(csv_file)
    lat0, lon0, h0 = origin
    plot_path_arc(X, node_ids)  # ← Shows your perfect height arc

    nodes_enu_df = pd.DataFrame({
        'node_id': node_ids,
        'x_enu_m': X[:, 0],
        'y_enu_m': X[:, 1],
        'z_enu_m': X[:, 2],
        'origin_lat': lat0,
        'origin_lon': lon0,
        'origin_height': h0
    })
    nodes_enu_df.to_csv(os.path.join(output_dir, 'lora_nodes_enu.csv'), index=False)

    plot_nodes_enu(X, "LoRa Bridge Nodes", config['show_plots'])

    C_star, xb_star = find_optimal_C(X, config)

    # Final clustering
    if config['use_lora_fcm']:
        centers, U, iters = hybrid_fcm_3d_lora(
            X, C_star, min_dist_penalty=config['min_dist_penalty'],
            repulsion_strength=config['repulsion_strength']
        )
    else:
        centers, U, iters = hybrid_fcm_3d(X, C_star)

    centers = gentle_min_distance_push(centers, X, config['min_dist_penalty'])
    centers = enforce_gateway_elevation(centers, X, config['min_elev_above_nodes'])

    min_dist_to_nodes = np.min(np.linalg.norm(X[:, None, :] - centers[None, :, :], axis=2), axis=0)
    print(f"\n✅ FINAL: C*={C_star}, XB={xb_star:.2e}")
    print(f"   Min gw-node distance: {min_dist_to_nodes.min():.1f}m (target >10m)")

    plot_nodes_and_gateways(X, centers, C_star, config['show_plots'])

    lat_gw, lon_gw, h_gw = local_enu_to_latlon(centers[:,0], centers[:,1], centers[:,2], lat0, lon0, h0)

    pd.DataFrame({
        'gateway_id': range(C_star),
        'longitude': lon_gw,
        'latitude': lat_gw,
        'height': h_gw
    }).to_csv(os.path.join(output_dir, 'lora_gateways_ns3.csv'), index=False)

    pd.DataFrame({
        'gateway_id': range(C_star),
        'x_enu_m': centers[:,0],
        'y_enu_m': centers[:,1],
        'z_enu_m': centers[:,2],
        'origin_lat': lat0, 'origin_lon': lon0, 'origin_height': h0
    }).to_csv(os.path.join(output_dir, 'lora_gateways_enu.csv'), index=False)

    print(f"\n✅ LoRa Bridge Optimized!")
    print(f"   Files saved to: {output_dir}")
    print("   Files: lora_gateways_ns3.csv + lora_gateways_enu.csv + lora_nodes_enu.csv")

    return C_star, centers, U

