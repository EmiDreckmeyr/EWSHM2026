    ax1.set_title('Height Arc Profile')
    ax1.grid(True, alpha=0.3)

    # XY trajectory (your actual layout)
    ax2.plot(X[:,0], X[:,1], 'o-', linewidth=3, markersize=8)
    ax2.scatter(X[0,0], X[0,1], c='green', s=200, marker='*', label='Node 0')
