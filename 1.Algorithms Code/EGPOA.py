def latlon_to_local_enu(lat, lon, height, lat0, lon0, height0=0.0):
    lat  = np.asarray(lat, dtype=float)
    lon  = np.asarray(lon, dtype=float)

    lat_rad  = np.radians(lat)
    lon_rad  = np.radians(lon)
    lat0_rad = np.radians(lat0)
    lon0_rad = np.radians(lon0)

    R = 6371000.0
    dlon = lon_rad - lon0_rad
    dlat = lat_rad - lat0_rad

    x = dlon * np.cos(lat0_rad) * R    # East
    y = dlat * R                       # North
    z = np.asarray(height) - height0   # Up

    return np.column_stack([x, y, z])

def local_enu_to_latlon(x, y, z, lat0, lon0, height0=0.0):
    R = 6371000.0
    lat0_rad = np.radians(lat0)
    lon0_rad = np.radians(lon0)

    lat_rad = lat0_rad + y / R
    lon_rad = lon0_rad + x / (R * np.cos(lat0_rad))
    height = height0 + z

    return np.degrees(lat_rad), np.degrees(lon_rad), height

def load_nodes_csv_to_xyz(csv_file):
    df = pd.read_csv(csv_file)
    lat, lon, height = df['latitude'].values, df['longitude'].values, df['height_m'].fillna(0).values


    lat0, lon0 = lat[0], lon[0]
    height0 = 0.0

    print(f"🌍 Arc origin: Node 0 at ({lat0:.8f}, {lon0:.8f})")

    X = latlon_to_local_enu(lat, lon, height, lat0, lon0, height0)
    return X, (lat0, lon0, height0), df['node_id'].values

