def plot_path_arc(X, node_ids, title="Node Path"):
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(15, 5))

    # Path distance (reveals true arc)
    path_dist = np.concatenate([[0], np.cumsum(np.linalg.norm(np.diff(X, axis=0), axis=1))])

    # Height vs path distance = PERFECT parabola
    ax1.plot(path_dist, X[:,2], 'o-', linewidth=3, markersize=8)
