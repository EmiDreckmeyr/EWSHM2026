def gentle_min_distance_push(centers, X, min_dist=10.0, push_factor=0.5):
    """GENTLE push away from nodes - no hard constraint."""
    centers = centers.copy()
    for i in range(len(centers)):
        dists = np.linalg.norm(X - centers[i], axis=1)
        nearest_node_idx = np.argmin(dists)
        current_dist = dists[nearest_node_idx]

        if current_dist < min_dist:
            # Gentle proportional push
            push_distance = (min_dist - current_dist) * push_factor
            direction = centers[i] - X[nearest_node_idx]
            direction /= np.linalg.norm(direction) + 1e-8
            centers[i] = X[nearest_node_idx] + direction * (current_dist + push_distance)

    return centers

def enforce_gateway_elevation(centers, X_nodes, min_elev_above_nodes):
    """Ensure ALL gateways are elevated above highest node + buffer."""
    centers = centers.copy()
    max_node_height = np.max(X_nodes[:, 2])
    target_height = max_node_height + min_elev_above_nodes

    # Preserve XY cluster centers, elevate Z only
    centers[:, 2] = np.maximum(centers[:, 2], target_height)

    print(f"🗼 Elevated gateways: max_node_h={max_node_height:.1f}m → target_h={target_height:.1f}m")
    print(f"   New heights: {centers[:, 2].round(1)}m")

    return centers

def lora_bridge_C_max(X, lora_coverage_m, node_ratio=15, hard_cap=10):
    N = len(X)
    span_xy = np.max(np.linalg.norm(X[:, :2] - np.mean(X[:, :2], axis=0), axis=1)) * 2 + 100

    C_coverage = max(1, int(np.ceil(span_xy / lora_coverage_m)))
    C_nodes = max(1, N // node_ratio)

    print(f"🌉 Bridge span: {span_xy - 100:.0f}m")
    print(f"📡 LoRa coverage: {lora_coverage_m}m/gateway")
    print(f"👥 Node rule: 1 gw per {node_ratio} nodes")

    return min(C_coverage, C_nodes, hard_cap) + 1

def find_optimal_C(X, config):
    C_max = lora_bridge_C_max(X, config['lora_coverage_m'],
                            config['node_ratio'], config['hard_cap'])
    C_min = 1
    N = len(X)
    max_nodes_per_gw = 40
    C_min = max(C_min, int(np.ceil(N / max_nodes_per_gw)) - 1)

    print(f"\n🔍 Testing C=[{C_min}, {C_max}] for LoRa bridge...")

    best_C, best_xb = C_min, np.inf
    for C in range(C_min, C_max + 1):
        if config['use_lora_fcm']:
            centers, U, iters = hybrid_fcm_3d_lora(
                X, C, min_dist_penalty=config['min_dist_penalty'],
                repulsion_strength=config['repulsion_strength']
            )
        else:
            centers, U, iters = hybrid_fcm_3d(X, C)

        # GENTLE push only if needed
        centers = gentle_min_distance_push(centers, X, config['min_dist_penalty'])

        xb = xie_beni_index_3d(X, centers, U)

        min_dist_to_nodes = np.min(np.linalg.norm(X[:, None, :] - centers[None, :, :], axis=2), axis=0)
        min_all = min_dist_to_nodes.min()

        status = "✓" if min_all >= 10 else "⚠️"
        print(f"  C={C}: XB={xb:.6e}, min gw-node={min_all:.1f}m {status}")

        if xb < best_xb:
            best_xb = xb
            best_C = C

    return best_C, best_xb

